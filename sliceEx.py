s1 = 'Hello'
s2 = s1[1:3]
print(s2)  # el

for c in s1:
    print(c)  # 文字列はそもそも配列のリストだから1文字ずつ取り出せる

l1 = [10, 20, 30, 40, 50]
l2 = l1[1:4]
print(l2)  # [20, 30, 40]

l2 = l1[3:]
print(l2)  # [40, 50]

l2 = l1[:3]
print(l2)  # [10, 20, 30]

l2 = l1[:]
print(l2)  # [10, 20, 30, 40, 50]

l2 = l1[-2:]
print(l2)  # [40, 50]

l2 = l1[2:100]
print(l2)  # [30, 40, 50]

l1 = [10, 20, 30, 40, 50, 60, 70, 80]
l2 = l1[1::2]  # [20, 40, 60]

l2 = l1[::-1]  # 全取得ステップを-1に設定で逆順取得
print(l2)  # [80, 70, 60, 50, 40, 30, 20, 10]
